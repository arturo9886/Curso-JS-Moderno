// INCREMENTOS o DECREMENTOS

/*PARA HACER UN INCREMENTO PONEMOS ++ delante o atras.
Al ponerlo antes de la variable, automaticamente se afectara con el
incremento o el decremento.afectara

Al ponerlo despues de la variable, la proxima vez que se llame, se
aplicara el aumento o decrecion.

*/

let puntaje = 5;

++puntaje;

console.log(puntaje);

//delante de la variable

let puntaje2 = 6;

puntaje2++;
console.log(puntaje2);




//DECREMENTO
let puntaje3 = 5;

--puntaje3;

console.log(puntaje3);

//delante de la variable

let puntaje4 = 6;

puntaje4--;
console.log(puntaje4);

/////////////////////////////////////INCREMENTAR MAS DE UN VALOR//////////////////////////////////////////

//////incremento

let puntaje5 = 5;

puntaje5+=3;

console.log(puntaje5);

//DECREMENTO

let puntaje6 = 6;

puntaje6-=3;

console.log(puntaje6);